package org.iitb.exam.main.wrappers;

import java.util.Vector;

public class Question {
	
	/**
	 * This class is used to set and get the details of question belongs to
	 * particular exam 
	 * 
	 * @author dipti
	 */

	/*
	 * @param QuestionID - for storing the particular question ID
	 * @param QuestionText- For storing the question text
	 * @param QuestionType- for storing type of question, 1-single choice, 2-Multiple choice, 3-sequencing
	 * @param OptionList - For storing all option value for that particular question
	 */
	private int QuestionID;
	private String QuestionText;
	private int QuestionType;
	private Vector<Option> OptionList=new Vector <Option>();
	/**
	 * @return the questionID
	 */
	public int getQuestionID() {
		return QuestionID;
	}
	/**
	 * @param questionID the questionID to set
	 */
	public void setQuestionID(int questionID) {
		QuestionID = questionID;
	}
	/**
	 * @return the questionText
	 */
	public String getQuestionText() {
		return QuestionText;
	}
	/**
	 * @param questionText the questionText to set
	 */
	public void setQuestionText(String questionText) {
		QuestionText = questionText;
	}
	/**
	 * @return the questionType
	 */
	public int getQuestionType() {
		return QuestionType;
	}
	/**
	 * @param questionType the questionType to set
	 */
	public void setQuestionType(int questionType) {
		QuestionType = questionType;
	}
	/**
	 * @return the optionList
	 */
	public Vector<Option> getOptionList() {
		return OptionList;
	}
	/**
	 * @param optionList the optionList to set
	 */
	public void setOptionList(Vector<Option> optionList) {
		OptionList = optionList;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Question [QuestionID=" + QuestionID + ", QuestionText="
				+ QuestionText + ", QuestionType=" + QuestionType
				+ ", OptionList=" + OptionList + "]";
	}
	
	
}
